export function getDetailPostBySlug(slug) {
  const raw = Array.isArray(slug) ? slug[0] : slug;
  if (!raw) return undefined;
  const normalized = decodeURIComponent(String(raw).trim()).toLowerCase();
  return detailPosts.find((p) => String(p.slug).toLowerCase() === normalized);
}
